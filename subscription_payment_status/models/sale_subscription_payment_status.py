from odoo import fields, models


class SaleSubscriptionPaymentStatus(models.Model):
    _name = "sale.subscription.payment_status"
    _description = "Sale Subscription Payment Status"

    name = fields.Char(
        translate=True,
        required=True,
    )
    code = fields.Char(
        string="Code",
        required=True,
    )
    active = fields.Boolean(
        string="Active",
        default=True,
    )
    note = fields.Text(
        string="Note",
    )
